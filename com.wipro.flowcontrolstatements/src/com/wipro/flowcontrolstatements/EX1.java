package com.wipro.flowcontrolstatements;

public class EX1 {
	public static void main(String[] args) 
    {
        int number=18;
        if(number > 0)
        {
            System.out.println(number+" is a positive number");
        }
        else if(number < 0)
        {
            System.out.println(number+" is a negative number");
        }
        else
        {
            System.out.println(number+" is zero");
        }
    }

}
